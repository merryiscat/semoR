package com.semo.alarm.character

/**
 * 세모알 알람 캐릭터의 상태를 정의하는 enum
 * 각 상태에 따라 다른 애니메이션이 재생됩니다.
 */
enum class CharacterState {
    /**
     * 기본 대기 상태
     * - 평상시 알람 대기 중일 때
     * - 부드러운 호흡, 꼬리 살짝 움직임
     */
    IDLE,
    
    /**
     * 등장 중 상태
     * - 알람이 처음 시작될 때
     * - 알에서 부화하는 애니메이션
     */
    APPEARING,
    
    /**
     * 관심 끌기 상태
     * - 알람이 울린 지 30초 후
     * - 고개 좌우, 앞발 들기 등 가벼운 움직임
     */
    ATTENTION,
    
    /**
     * 회전 중 상태 (핵심 기능)
     * - 알람이 울린 지 1분 후
     * - 360도 회전으로 강력한 시선 집중
     * - 세모알 브랜드의 핵심 캐릭터 기능
     */
    SPINNING,
    
    /**
     * 독촉 모드 상태
     * - 알람이 울린 지 3분 후
     * - 격렬한 움직임, 입 벌리고 울기
     * - 네온 블루 하이라이트 효과 적용
     */
    URGENT,
    
    /**
     * 비활성화 상태
     * - 알람이 꺼졌거나 스누즈 상태
     * - 딥 그레이 색상으로 페이드
     */
    SLEEPING
}